// Update contact form
function updateContactForm(t) {
    const formLabels = document.querySelectorAll('.contact-form label');
    formLabels.forEach(label => {
        const forAttr = label.getAttribute('for');
        if (forAttr === 'name') {
            label.textContent = t.yourName + ' *';
        } else if (forAttr === 'email') {
            label.textContent = t.emailAddress + ' *';
        } else if (forAttr === 'phone') {
            label.textContent = t.phoneNumber;
        } else if (forAttr === 'subject') {
            label.textContent = t.subject + ' *';
        } else if (forAttr === 'message') {
            label.textContent = t.message + ' *';
        }
    });
    
    const selectOptions = document.querySelectorAll('#subject option');
    if (selectOptions.length > 0) {
        selectOptions[0].textContent = t.selectSubject;
        if (selectOptions.length > 1) selectOptions[1].textContent = t.generalInquiry;
        if (selectOptions.length > 2) selectOptions[2].textContent = t.eventsPrograms;
        if (selectOptions.length > 3) selectOptions[3].textContent = t.membership;
        if (selectOptions.length > 4) selectOptions[4].textContent = t.communitySupport;
        if (selectOptions.length > 5) selectOptions[5].textContent = t.educationPrograms;
        if (selectOptions.length > 6) selectOptions[6].textContent = t.other;
    }
    
    const submitBtn = document.querySelector('.contact-form button[type="submit"]');
    if (submitBtn) {
        submitBtn.textContent = t.send;
    }
}

