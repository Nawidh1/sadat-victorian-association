// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'en';
}

// Set language preference
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    applyTranslations(lang);
}

// Apply translations to the page
function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;
    
    // Update direction
    document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    
    // Update navigation - use data-translate if available, otherwise use href matching
    const navLinks = document.querySelectorAll('.main-nav a');
    navLinks.forEach(link => {
        const translateKey = link.getAttribute('data-translate');
        if (translateKey && t[translateKey]) {
            link.textContent = t[translateKey];
        } else {
            // Fallback to href matching
            const href = link.getAttribute('href');
            if (href === 'index.php' || href === 'index.html' || href === '') {
                link.textContent = t.home;
            } else if (href.includes('about')) {
                link.textContent = t.about;
            } else if (href.includes('events')) {
                link.textContent = t.events;
            } else if (href.includes('resources')) {
                link.textContent = t.resources;
            } else if (href.includes('news')) {
                link.textContent = t.news;
            } else if (href.includes('contact')) {
                link.textContent = t.contact;
            }
        }
    });
    
    // Update ALL elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (key && t[key]) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = t[key];
            } else if (element.tagName === 'OPTION') {
                element.textContent = t[key];
            } else if (element.tagName === 'LI') {
                // For list items, check if translation contains colon for formatting
                const translation = t[key];
                if (translation) {
                    if (translation.includes(':')) {
                        // Format with bold for part before colon
                        element.innerHTML = translation.replace(/([^:]+):\s*(.+)/, '<strong>$1:</strong> $2');
                    } else {
                        // No colon, just replace text
                        element.textContent = translation;
                    }
                }
            } else if (element.tagName === 'H2' || element.tagName === 'H3' || element.tagName === 'H1') {
                // For headings, always update text content
                element.textContent = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });
    
    // Update specific elements by ID
    updateElementText('hero-title', t.welcomeTitle);
    updateElementText('hero-subtitle', t.welcomeSubtitle);
    updateElementText('learn-more-btn', t.learnMore);
    updateElementText('upcoming-events-btn', t.upcomingEvents);
    updateElementText('mission-title', t.ourMission);
    updateElementText('mission-subtitle', t.missionSubtitle);
    
    // Update feature cards
    updateFeatureCards(t);
    
    // Update news section
    updateNewsSection(t, lang);
    
    // Update page headers
    updatePageHeaders(t);
    
    // Update footer
    updateFooter(t);
    
    // Update contact form
    updateContactForm(t);
    
    // Update language toggle button
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        const langText = langToggle.querySelector('.lang-text');
        if (langText) {
            langText.textContent = lang === 'en' ? 'فارسی' : 'English';
        } else {
            // Fallback if structure is different
            langToggle.textContent = lang === 'en' ? 'فارسی' : 'English';
        }
        langToggle.setAttribute('data-lang', lang === 'en' ? 'fa' : 'en');
        langToggle.setAttribute('title', lang === 'en' ? 'Switch to Farsi / تغییر به فارسی' : 'Switch to English / تغییر به انگلیسی');
        
        // Remove all existing click listeners by cloning
        const newLangToggle = langToggle.cloneNode(true);
        langToggle.parentNode.replaceChild(newLangToggle, langToggle);
        
        // Add click event listener
        newLangToggle.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            const currentLang = getCurrentLanguage();
            const newLang = currentLang === 'en' ? 'fa' : 'en';
            setLanguage(newLang);
        });
    }
    
    // Trigger admin content update if the script is loaded
    if (typeof window.updateAdminContent === 'function') {
        setTimeout(window.updateAdminContent, 100);
    }
    
    // Update dynamic content that might have been loaded after page load
    updateDynamicContent(t, lang);
    
    // Dispatch custom event for language change
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: lang } }));
}

